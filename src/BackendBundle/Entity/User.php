class User implements UserInterface {
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var string
     */
    private $surname;

    /**
     * @var string
     */
    private $username;

    /**
     * @var string
     */
    private $email;

    /**
     * @var string
     */
    private $password;

    /**
     * @var string
     */
    private $imame;

    /**
     * @var string
     */
    private $role;


    public function getSalt() {
        return null;        
    }
    
    public function getRoles() {
        return array('ROLE_TEACHER','ROLE_ADMIN');        
    }
    
    public function __toString() {
        return $this->name;
    }
    
     public function eraseCredentials() {
     
    }
    
    
